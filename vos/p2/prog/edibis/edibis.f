	    IF (COLFORMCODE(CURCOL) .EQ. FMT_REAL) THEN
		MINVAL = -1.6E38
	    ELSE IF (COLFORMCODE(CURCOL) .EQ. FMT_FULL) THEN
		MINVAL_FULL = -2147483647
