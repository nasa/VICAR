	    ELSE IF (COLFORMCODE(CURCOL) .EQ. FMT_A64) THEN
		MINVAL_A64 = RANGESTRING(1:LEN1)
	    ENDIF
	ENDIF

	IF ((I.EQ.STRLEN).AND.(STRLEN.GT.0)) THEN  ! MIN: OPTION
	    IF (COLFORMCODE(CURCOL) .EQ. FMT_REAL) THEN
