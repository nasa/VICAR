		MINVAL_A16 = '                '
	    ELSE IF (COLFORMCODE(CURCOL) .EQ. FMT_A32) THEN
		MINVAL_A32 = '                                '
	    ELSE IF (COLFORMCODE(CURCOL) .EQ. FMT_A64) THEN
